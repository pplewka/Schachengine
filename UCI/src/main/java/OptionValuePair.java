/**
 * Data class for storing an option with its value
 */
public class OptionValuePair {
    public final String option;
    public final String value;

    /**
     * Constructor
     * @param option the option
     * @param value the value
     */
    public OptionValuePair(String option, String value){
        this.option = option;
        this.value = value;
    }

}
